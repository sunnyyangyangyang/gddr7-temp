#!/usr/bin/env python3
"""Generate gpu_offsets_generated.h from offsets.yaml.

Produces a standalone header with the gpu_tables[] array definition.
The skeleton (struct + extern declarations) lives in gpu_offsets.h."""
import sys
import yaml


def to_c_hex(v):
    if isinstance(v, str) and v.startswith("0x"):
        return v
    return hex(int(v))


def escape_c_string(s: str) -> str:
    """Escape backslashes and double quotes for C string literals."""
    return s.replace("\\", "\\\\").replace('"', '\\"')


def _generate_entries(tables):
    """Generate C array initializer entries from YAML tables.

    Each entry may have 'dqr', 'therm', or both blocks. Missing blocks
    default to all-zero fields (num_modules/num_channels == 0 means absent)."""
    seen_ids = set()
    for t in tables:
        dev_id = int(t["device_id"], 16) if isinstance(t["device_id"], str) else t["device_id"]
        if dev_id in seen_ids:
            sys.exit(f"gen_offsets: duplicate device_id 0x{dev_id:04x}")
        seen_ids.add(dev_id)

        dqr = t.get("dqr", {})
        therm = t.get("therm", {})

        yield (
            f'    {{ .device_id = 0x{dev_id:04x}, .name = "{escape_c_string(t["name"])}",\n'
            f'      .dqr_module0 = {to_c_hex(dqr.get("module0", 0))}, '
            f'.dqr_vld_off = {to_c_hex(dqr.get("vld_off", 0))},\n'
            f'      .dqr_stride = {to_c_hex(dqr.get("stride", 0))}, '
            f'.dqr_num_modules = {dqr.get("num_modules", 0)},\n'
            f'      .therm_ch0 = {to_c_hex(therm.get("ch0", 0))}, '
            f'.therm_ch_stride = {to_c_hex(therm.get("stride", 0))},\n'
            f'      .therm_num_channels = {therm.get("num_channels", 0)} }},'
        )


def main(yaml_path, out_path):
    with open(yaml_path) as f:
        tables = yaml.safe_load(f)

    entries_text = "\n".join(_generate_entries(tables))

    header = """\
/* gpu_offsets_generated.h — auto-generated by gen_offsets.py. DO NOT EDIT. */

#ifndef GPU_OFFSETS_GENERATED_H
#define GPU_OFFSETS_GENERATED_H

#include "gpu_offsets.h"

const struct gpu_offset_table gpu_tables[] = {
"""
    footer = f"""\
}};

const int gpu_tables_count = {len(tables)};

#endif /* GPU_OFFSETS_GENERATED_H */
"""

    with open(out_path, "w") as f:
        f.write(header + entries_text + "\n" + footer)


if __name__ == "__main__":
    main(sys.argv[1], sys.argv[2])
